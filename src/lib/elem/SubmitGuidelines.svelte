<main>
	<h3>How To Submit a Word</h3>
	<h4>Etiquette</h4>
	<ul class="list">
		<li>
			<b>Be thoughtful.</b> try and think about how you define your word. Select the proper type (verbs
			for action words, adjectives for descriptors, etc.)
		</li>
		<li>
			<b>Be respectful.</b> absolutely no hatespeech is tolerated and will result in a ban on submitting
			words
		</li>
		<li>
			<b>Be kind.</b> The Curio aims to foster a wholesome and accepting community. If you come here
			with the intention of disrupting things you will not be recieved well.
		</li>
	</ul>

	<h4>Formatting</h4>
	<ul class="list">
		<li>
			<b>Italics.</b> surrounding words in asterisks like *this* will make words italic like
			<i>this</i>
		</li>
		<li>
			<b>Hyperlinks.</b>
			surrounding words in double square brackets like [[this]] will turn the surrounded word into a
			hyperlink to that word in The Curio
		</li>
	</ul>
</main>

